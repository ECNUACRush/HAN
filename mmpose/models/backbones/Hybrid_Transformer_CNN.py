class HTC(BaseModule):
    """
    Args:
        pretrain_img_size (int | tuple[int]): The size of input image when
            pretrain. Defaults: 224.
        in_channels (int): Number of input channels. Default: 3.
        embed_dims (int): Embedding dimension. Default: 64.
        num_stags (int): The num of stages. Default: 4.
        num_layers (Sequence[int]): The layer number of each transformer encode
            layer. Default: [3, 4, 6, 3].
        num_heads (Sequence[int]): The attention heads of each transformer
            encode layer. Default: [1, 2, 5, 8].
        patch_sizes (Sequence[int]): The patch_size of each patch embedding.
            Default: [4, 2, 2, 2].
        strides (Sequence[int]): The stride of each patch embedding.
            Default: [4, 2, 2, 2].
        paddings (Sequence[int]): The padding of each patch embedding.
            Default: [0, 0, 0, 0].
        sr_ratios (Sequence[int]): The spatial reduction rate of each
            transformer encode layer. Default: [8, 4, 2, 1].
        out_indices (Sequence[int] | int): Output from which stages.
            Default: (0, 1, 2, 3).
        mlp_ratios (Sequence[int]): The ratio of the mlp hidden dim to the
            embedding dim of each transformer encode layer.
            Default: [8, 8, 4, 4].
        qkv_bias (bool): Enable bias for qkv if True. Default: True.
        drop_rate (float): Probability of an element to be zeroed.
            Default 0.0.
        attn_drop_rate (float): The drop out rate for attention layer.
            Default 0.0.
        drop_path_rate (float): stochastic depth rate. Default 0.1.
        use_abs_pos_embed (bool): If True, add absolute position embedding to
            the patch embedding. Defaults: True.
        use_conv_ffn (bool): If True, use Convolutional FFN to replace FFN.
            Default: False.
        act_cfg (dict): The activation config for FFNs.
            Default: dict(type='GELU').
        norm_cfg (dict): Config dict for normalization layer.
            Default: dict(type='LN').
        pretrained (str, optional): model pretrained path. Default: None.
        convert_weights (bool): The flag indicates whether the
            pre-trained model is from the original repo. We may need
            to convert some keys to make it compatible.
            Default: True.
        init_cfg (dict or list[dict], optional): Initialization config dict.
            Default: None.
    """

    def __init__(self,
                 pretrain_img_size=224,
                 in_channels=3,
                 embed_dims=64,
                 num_stages=4,
                 num_layers=[3, 4, 6, 3],
                 num_heads=[1, 2, 5, 8],
                 patch_sizes=[4, 2, 2, 2],
                 strides=[4, 2, 2, 2],
                 paddings=[0, 0, 0, 0],
                 out_indices=(0, 1, 2, 3),
                 mlp_ratios=[8, 8, 4, 4],
                 chratio = [1,1,1,1],

                 qk_dims=[None, None, None, None, None],
                 nheads=[2, 4, 10, 16],  # embed_dim/head_dim [2,4,10,16]
                 head_dim=64,
                 qk_scale=None,
                 representation_size=None,
                 # drop_path_rate=0., drop_rate=0.,
                 use_checkpoint_stages=[],
                 n_win=7,
                 window_size=[16, 16, 8, 8],
                 kv_downsample_mode='identity',
                 kv_per_wins=[-1, -1, -1, -1],
                 topks=[1, 1, 1, -2],
                 side_dwconv=5,
                 before_attn_dwconv=3,
                 auto_pad=True,
                 kv_downsample_kernels=[4, 2, 1, 1],#[4, 2, 1, 1]
                 kv_downsample_ratios=[4, 2, 1, 1],  # -> kv_per_win = [2, 2, 2, 1]
                 layer_scale_init_value=-1,
                 param_routing=False, diff_routing=False, soft_routing=False,
                 pre_norm=True,
                 pe=None,
                 pe_stages=[0],
                 # mlp_ratios=[4, 4, 4, 4],
                 param_attention='qkvo',
                 mlp_dwconv=False,
                 # kv_downsample_mode='identity',

                 qkv_bias=True,
                 drop_rate=0.,
                 attn_drop_rate=0.,
                 drop_path_rate=0.1,
                 norm_after_stage=False,
                 use_conv_ffn=False,
                 act_cfg=dict(type='GELU'),
                 norm_cfg=dict(type='BN'),#dict(type='LN', eps=1e-6),
                 pretrained=None,
                 convert_weights=False,
                 init_cfg=None):
        super().__init__(init_cfg=init_cfg)

        self.convert_weights = convert_weights
        if isinstance(pretrain_img_size, int):
            pretrain_img_size = to_2tuple(pretrain_img_size)
        elif isinstance(pretrain_img_size, tuple):
            if len(pretrain_img_size) == 1:
                pretrain_img_size = to_2tuple(pretrain_img_size[0])
            assert len(pretrain_img_size) == 2, \
                f'The size of image should have length 1 or 2, ' \
                f'but got {len(pretrain_img_size)}'

        self.embed_dims = embed_dims

        self.num_stages = num_stages
        self.num_layers = num_layers
        self.num_heads = num_heads
        self.patch_sizes = patch_sizes
        self.strides = strides
        self.chratio = chratio
        assert num_stages == len(num_layers) == len(num_heads) \
               == len(patch_sizes) == len(strides)

        self.out_indices = out_indices
        assert max(out_indices) < self.num_stages
        self.pretrained = pretrained

        # transformer encoder
        dpr = [
            x.item()
            for x in torch.linspace(0, drop_path_rate, sum(num_layers))
        ]  # stochastic num_layer decay rule

        cur = 0
        '''self.preconv = Bottleneck(
            in_channels=in_channels,
            out_channels=embed_dims,
        )'''
        self.conv_lay = ModuleList()
        for i in range(num_stages):
            embed_dims_ic = embed_dims * (num_heads[i])
            self.conv_lay.extend([
                BasicBlock(
                    in_channels=embed_dims_ic,
                    out_channels=embed_dims_ic,
                )
            ])
            embed_dims_ic = embed_dims_ic
        self.layers = ModuleList()
        for i, num_layer in enumerate(num_layers):
            embed_dims_i = embed_dims * (num_heads[i])
            patch_embed = PatchEmbed_new(
                in_channels=in_channels,
                embed_dims=embed_dims_i,
                kernel_size=patch_sizes[i],
                stride=strides[i],
                padding=paddings[i],
                bias=True,
                norm_cfg=norm_cfg)
            drop_path = dpr[sum(num_layers[:i]):sum(num_layers[:i + 1])]

            layers = ModuleList()
            layers.extend([
                Block(dim=embed_dims_i, drop_path=drop_path[idx] if isinstance(drop_path, list) else drop_path,
                      layer_scale_init_value=layer_scale_init_value,
                      topk=topks[i],
                      num_heads=nheads[i],
                      n_win=window_size[i],
                      # n_win=n_win,
                      qk_dim=qk_dims[i],
                      qk_scale=qk_scale,
                      kv_per_win=kv_per_wins[i],
                      kv_downsample_ratio=kv_downsample_ratios[i],
                      kv_downsample_kernel=kv_downsample_kernels[i],
                      kv_downsample_mode=kv_downsample_mode,
                      param_attention=param_attention,
                      param_routing=param_routing,
                      diff_routing=diff_routing,
                      soft_routing=soft_routing,
                      mlp_ratio=mlp_ratios[i],
                      mlp_dwconv=mlp_dwconv,
                      side_dwconv=side_dwconv,
                      before_attn_dwconv=before_attn_dwconv,
                      pre_norm=pre_norm,
                      auto_pad=auto_pad) for idx in range(num_layer)
            ])
            in_channels = embed_dims_i
            # The ret[0] of build_norm_layer is norm name.
            if norm_after_stage:
                norm = build_norm_layer(norm_cfg, embed_dims_i)[1]
            else:
                norm = nn.Identity()
            self.layers.append(ModuleList([patch_embed, layers, norm]))
            cur += num_layer

    def init_weights(self, pretrained=None):
        if isinstance(pretrained, str):
            logger = get_root_logger()
            load_checkpoint(self, pretrained, strict=False, logger=logger)
        else:
            for m in self.modules():
                if isinstance(m, nn.Linear):
                    trunc_normal_init(m.weight, std=.02)
                    if m.bias is not None:
                        constant_init(m.bias, 0)
                elif isinstance(m, nn.LayerNorm):
                    constant_init(m.bias, 0)
                    constant_init(m.weight, 1.0)
                elif isinstance(m, nn.Conv2d):
                    fan_out = m.kernel_size[0] * m.kernel_size[
                        1] * m.out_channels
                    fan_out //= m.groups
                    normal_init(m.weight, 0, math.sqrt(2.0 / fan_out))
                    if m.bias is not None:
                        constant_init(m.bias, 0)


    def forward(self, x):
        outs = []
        for i, layer in enumerate(self.layers):
            x, hw_shape = layer[0](x)
            for block in layer[1]:
                x = block(x)
            x = layer[2](x)

            x = self.conv_lay[i](x)

            if i in self.out_indices:
                outs.append(x)
        if len(outs) == 1:
            return outs[0]
        # outs = self.forward_downfeatures(outs)
        return outs

